  function applyLookupCorrections(text) {
    let corrected = text;
    let hasCorrection = false;
    const lookupTable = CORRECTIONS.lookupDe;
    // 使用正則匹配所有可能的錯誤詞組
    // 策略：先匹配最長的詞組，避免部分匹配問題
    const sortedKeys = Object.keys(lookupTable).sort((a, b) => b.length - a.length);
    for (const wrong of sortedKeys) {
      const correct = lookupTable[wrong];
      const regex = new RegExp(wrong.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'g');
      if (regex.test(corrected)) {
        corrected = corrected.replace(regex, correct);
        hasCorrection = true;
      }
    }
    return { text: corrected, changed: hasCorrection };
  }
