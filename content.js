  // 執行語境判斷修正
  function applyContextualCorrections(text, fullContext) {
    let corrected = text;
    let hasCorrection = false;
    // 處理應該用「再」的情況
    for (const rule of CORRECTIONS.contextual.shouldBeZai4) {
      rule.pattern.lastIndex = 0;
      const matches = corrected.match(rule.pattern);
      if (matches) {
        if (!rule.check) {
          // 無需語境檢查，直接用 regex 替換（正確解析 $1）
          rule.pattern.lastIndex = 0;
          corrected = corrected.replace(rule.pattern, rule.correction);
          hasCorrection = true;
        } else {
          // 需要語境檢查，逐個比對
          for (const match of matches) {
            const index = corrected.indexOf(match);
            const beforeContext = fullContext.slice(0, fullContext.indexOf(match) + index);
            const afterContext = fullContext.slice(fullContext.indexOf(match) + index + match.length);
            if (rule.check(beforeContext, afterContext)) {
              const singlePattern = new RegExp(rule.pattern.source, rule.pattern.flags.replace('g', ''));
              corrected = corrected.replace(singlePattern, rule.correction);
              hasCorrection = true;
            }
          }
        }
      }
    }
    // 處理應該用「在」的情況
    for (const rule of CORRECTIONS.contextual.shouldBeZai) {
      if (rule.pattern.test(corrected)) {
        corrected = corrected.replace(rule.pattern, rule.correction);
        hasCorrection = true;
      }
    }
    // 處理應該用「哪」的情況（疑問語境）
    for (const rule of CORRECTIONS.contextual.shouldBeNa3) {
      rule.pattern.lastIndex = 0;
      const matches = corrected.match(rule.pattern);
      if (matches) {
        if (!rule.check) {
          rule.pattern.lastIndex = 0;
          corrected = corrected.replace(rule.pattern, rule.correction);
          hasCorrection = true;
        } else {
          for (const match of matches) {
            const index = corrected.indexOf(match);
            const beforeContext = fullContext.slice(0, fullContext.indexOf(match) + index);
            const afterContext = fullContext.slice(fullContext.indexOf(match) + index + match.length);
            if (rule.check(beforeContext, afterContext)) {
              const singlePattern = new RegExp(rule.pattern.source, rule.pattern.flags.replace('g', ''));
              corrected = corrected.replace(singlePattern, rule.correction);
              hasCorrection = true;
            }
          }
        }
      }
    }
    // 處理應該用「那」的情況（指示語境）
    for (const rule of CORRECTIONS.contextual.shouldBeNa4) {
      rule.pattern.lastIndex = 0;
      const matches = corrected.match(rule.pattern);
      if (matches) {
        if (!rule.check) {
          rule.pattern.lastIndex = 0;
          corrected = corrected.replace(rule.pattern, rule.correction);
          hasCorrection = true;
        } else {
          for (const match of matches) {
            const index = corrected.indexOf(match);
            const beforeContext = fullContext.slice(0, fullContext.indexOf(match) + index);
            const afterContext = fullContext.slice(fullContext.indexOf(match) + index + match.length);
            if (rule.check(beforeContext, afterContext)) {
              const singlePattern = new RegExp(rule.pattern.source, rule.pattern.flags.replace('g', ''));
              corrected = corrected.replace(singlePattern, rule.correction);
              hasCorrection = true;
            }
          }
        }
      }
    }
    return { text: corrected, changed: hasCorrection };
  }
